// Helper function to get all products
export function getAllProducts() {
  return [...Object.values(generators), ...Object.values(waterPumps), ...Object.values(tillerMachines)]
}

// Helper function to get product by slug
export function getProductBySlug(slug: string) {
  return (
    generators[slug as keyof typeof generators] ||
    waterPumps[slug as keyof typeof waterPumps] ||
    tillerMachines[slug as keyof typeof tillerMachines] ||
    null
  )
}

// Helper function to get products by category
export function getProductsByCategory(category: string) {
  switch (category.toLowerCase()) {
    case "generator":
    case "generators":
      return Object.values(generators)
    case "water pump":
    case "water pumps":
      return Object.values(waterPumps)
    case "tiller machine":
    case "tiller machines":
    case "power weeder":
    case "power weeders":
      return Object.values(tillerMachines)
    default:
      return []
  }
}

// Helper function to get diesel water pumps
export function getDieselWaterPumps() {
  return Object.values(waterPumps).filter((pump) => pump.subCategory === "Diesel Water Pump")
}

// Helper function to get petrol water pumps
export function getPetrolWaterPumps() {
  return Object.values(waterPumps).filter((pump) => pump.subCategory === "Petrol Water Pump")
}
